<div id="step-one" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step One</h3>
    <h2>Initial Meeting</h2>
        
	<p>All necessary parties will meet to discuss project in detail. Attendees will usually include client, Branding Specialist, Content Specialist, Project Manager, UX Specialist, and Developer.</p>
  
</div><!--step-one-->

<div id="step-two" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Two</h3>
    <h2>Branding</h2>
        
	<p>Branding Specialists will design a new identity, with deliverables including a three versions: horizontal, vertical, and social badge size; one color and full color options. Full branding package includes a custom color palette, typography guidelines, logo usage guidelines, business card template, and letterhead template. Quoted expenses include two rounds of revisions.</p>
  
</div><!--step-two-->

<div id="step-three" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Three</h3>
    <h2>Competitive Analysis</h2>
        
	<p>Content Specialist will write a Competitive Analysis based on competitors identified by client. Client will be asked to approve Competitive Analysis.</p>
  
</div><!--step-three-->

<div id="step-four" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Four</h3>
    <h2>Site Audit</h2>
        
	<p>Developer will complete a site audit of existing site, if applicable. Metrics analyzed will include Google Analytics, speed test, and mobile friendliness test. Client will be asked to approve Site Audit. </p>
  
</div><!--step-four-->

<div id="step-five" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Five</h3>
    <h2>Site Map</h2>
        
	<p>Based on findings from Site Audit, developer will use an online application to plan new website. Client will be asked to approve Site Map.</p>
  
</div><!--step-five-->

<div id="step-six" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Six</h3>
    <h2>Wireframes</h2>
        
	<p>Using an online application, UX Specialist will create Wireframes for the purpose of displaying user experience, flow, placement of calls to action, and rough page layouts. Client will be asked to approve Wireframes.</p>
  
</div><!--step-six-->

<div id="step-seven" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Seven</h3>
    <h2>Content Strategy</h2>
        
	<p>Content Specialist will work with client to: determine appropriate tone, voice, and message that appeals to audiences and matches the look and feel of the new website; and create style guide specific to client to ensure consistent messaging.</p>
  
</div><!--step-seven-->

<div id="step-eight" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Eight</h3>
    <h2>Content Creation</h2>
        
	<p>Content Specialist will work with SEO Specialist to create keyword-rich content for use on the website. Content Specialist will initially present client with questionnaires, followed by phone calls if necessary to clarify any questions. Quoted expenses includes two rounds of revisions to content. Client will be asked to approve content.</p>
  
</div><!--step-eight-->

<div id="step-nine" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Nine</h3>
    <h2>Design Comps</h2>
        
	<p>Using Photoshop, Graphic Designer will create an index page, subpages, a basic frame for tertiary pages, and an overlay layer that represents CMS-editable portions of site. Client will be asked to approve Design Comps.</p>
  
</div><!--step-nine-->

<div id="step-ten" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Ten</h3>
    <h2>Site Build</h2>
        
	<p>Using HTML, CSS, JQuery, and PHP, Front End Developer will code the website, writing usable, functional, responsive code that is optimized for all major browsers on all major operating systems and devices. Back End Developer will connect website to Sitemason, complete e-commerce sections, hook up any special protected audience areas, ensure forms submit properly, and hook up any online intake areas. </p>

	<ul>
		<li>website will follow best development practices as determined by industry standards</li>
		<li>website will use clean, solid, semantic markup with as little overhead and redundancy as possible</li>
		<li>website will be documented and commented for ease of maintenance</li>
	</ul>

</div><!--step-ten-->

<div id="step-eleven" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Eleven</h3>
    <h2>SEO</h2>
        
	<p>SEO Specialist will provide Search Engine Optimization, including Google Analytics, Google Web Toolkit, and Yext. SEO Specialist will handle all post-launch optimization. Steps taken will be reported to client. Ongoing SEO management is highly recommended. Terms and scope vary and will be agreed upon on a custom level.</p>
  
</div><!--step-eleven-->

<div id="step-twelve" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Twelve</h3>
    <h2>Testing</h2>
        
	<p>Developer will ensure website displays correctly on all major browsers (Firefox, Safari, Chrome, Internet Explorer 9-11) and operating systems and test responsive code thoroughly in each.</p>
  
</div><!--step-twelve-->

<div id="step-thirteen" style="display:none; max-width:1000px;" class="details">
  
    <h3>Step Thirteen</h3>
    <h2>Proofing</h2>
        
